fn main() {
    // addition
    let sum = 5 + 10;

    // subtraction
    let difference = 95.5 - 4.3;

    // multiplication
    let product = 4 * 30;

    // division
    let quotient = 56.7 / 32.2;
    let floored = 2 / 3; // Results in 0
    let modulo = 14 % 4; // 5 MOD 6

    // remainder
    let remainder = 43 % 5;

    println!("{}, {}, {}, {},\nmodulo is {},\n{}, {}", sum, difference, product, quotient, modulo, floored, remainder);
}
